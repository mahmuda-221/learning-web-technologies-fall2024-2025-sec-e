<?php

	// Define the length and width of the rectangle
		$length = 10; 
		$width = 5;   

	// Calculate the area
	$area = $length * $width;

	// Calculate the perimeter
	$perimeter = 2 * ($length + $width);

	// Display the results
	echo "The area of the rectangle is: " . $area . " square units.<br>";
	echo "The perimeter of the rectangle is: " . $perimeter . " units.";
?>
